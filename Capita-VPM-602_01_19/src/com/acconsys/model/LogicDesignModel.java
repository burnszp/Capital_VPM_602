package com.acconsys.model;

public class LogicDesignModel {

	private String name;
	private String revision;
	private String releaseLevel;
	private String createBy;
	private String createOn;
	private String lastModifiedBy;
	private String lastModifiedOn;
	private String lastCheckOutBy;
	private String lastCheckOutOn;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getRevision() {
		return revision;
	}

	public void setRevision(String revision) {
		this.revision = revision;
	}

	public String getReleaseLevel() {
		return releaseLevel;
	}

	public void setReleaseLevel(String releaseLevel) {
		this.releaseLevel = releaseLevel;
	}

	public String getCreateBy() {
		return createBy;
	}

	public void setCreateBy(String createBy) {
		this.createBy = createBy;
	}

	public String getCreateOn() {
		return createOn;
	}

	public void setCreateOn(String createOn) {
		this.createOn = createOn;
	}

	public String getLastModifiedBy() {
		return lastModifiedBy;
	}

	public void setLastModifiedBy(String lastModifiedBy) {
		this.lastModifiedBy = lastModifiedBy;
	}

	public String getLastModifiedOn() {
		return lastModifiedOn;
	}

	public void setLastModifiedOn(String lastModifiedOn) {
		this.lastModifiedOn = lastModifiedOn;
	}

	public String getLastCheckOutBy() {
		return lastCheckOutBy;
	}

	public void setLastCheckOutBy(String lastCheckOutBy) {
		this.lastCheckOutBy = lastCheckOutBy;
	}

	public String getLastCheckOutOn() {
		return lastCheckOutOn;
	}

	public void setLastCheckOutOn(String lastCheckOutOn) {
		this.lastCheckOutOn = lastCheckOutOn;
	}

}
